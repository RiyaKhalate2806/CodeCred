public class ArithmeticOperations {
    public static void main(String[] args) {
        int a = 28;
        int b = 36;
        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        double div = (a / b);
        System.out.println(a + " " + "+" + " " + b + " = " + sum);
        System.out.println(a + " " + "-" + b + " " + " = " + sub);
        System.out.println(a + " " + "*" + b + " " + " = " + mul);
        System.out.println(a + " " + "/" + b + " " + " = " + div);

    }
}
