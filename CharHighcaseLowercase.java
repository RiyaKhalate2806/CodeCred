public class CharHighcaseLowercase {
    public static void main(String[] args) {
        char ch = 'Y';
        if ((ch >= 'A' && ch <= 'Z')) {
            System.out.println("This is a Higher case");
        }
        else if(ch >= 'a' && ch <= 'z'){
            System.out.println("This is a Lower case");
        } 
    }
}